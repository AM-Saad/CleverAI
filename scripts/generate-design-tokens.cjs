#!/usr/bin/env node

const { readFileSync, writeFileSync } = require("node:fs");
const { resolve } = require("node:path");
const {
  rootTokens,
  themeTokens,
  darkTokens = [],
} = require("../app/design-system/tokens/index.cjs");

const root = process.cwd();
const cssOutput = resolve(root, "app/design-system/tokens.generated.css");
const tsOutput = resolve(root, "app/design-system/tokens.generated.ts");

function renderToken(token) {
  const comment = token.comment ? ` /* ${token.comment} */` : "";
  return `  ${token.name}: ${token.value};${comment}`;
}

function renderOutputs() {
  const css = `/* This file is generated by scripts/generate-design-tokens.cjs. Do not edit manually. */

@theme static {
${themeTokens.map(renderToken).join("\n")}
}

:root {
${rootTokens.map(renderToken).join("\n")}
}

/* Dark theme: same token names, overridden when an ancestor carries .dark
   (global toggle on <html>, or a scoped wrapper). Token utilities (bg-surface,
   text-content-*, ...) flip automatically. */
html.dark,
.dark {
${darkTokens.map(renderToken).join("\n")}
}
`;

  const flatTokens = Object.fromEntries(
    [...themeTokens, ...rootTokens].map((token) => [token.name, token.value]),
  );

  const ts = `/* This file is generated by scripts/generate-design-tokens.cjs. Do not edit manually. */

export const designTokenValues = ${JSON.stringify(flatTokens, null, 2)} as const;

export type DesignTokenName = keyof typeof designTokenValues;
`;

  return { css, ts };
}

const { css, ts } = renderOutputs();

if (process.argv.includes("--check")) {
  const stale = [
    [cssOutput, css],
    [tsOutput, ts],
  ].filter(([file, expected]) => {
    try {
      return readFileSync(file, "utf8") !== expected;
    } catch {
      return true;
    }
  });

  if (stale.length) {
    console.error(
      `[design-tokens] Stale generated output: ${stale.map(([file]) => file).join(", ")}`,
    );
    process.exit(1);
  }
  console.log("[design-tokens] Generated outputs are current.");
  process.exit(0);
}

writeFileSync(cssOutput, css);
writeFileSync(tsOutput, ts);

console.log(`[design-tokens] Wrote ${cssOutput}`);
console.log(`[design-tokens] Wrote ${tsOutput}`);

module.exports = { renderOutputs };
